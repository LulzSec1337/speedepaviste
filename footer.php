	// Enhanced back to top functionality with smooth animations
	document.addEventListener('DOMContentLoaded', function() {
		const backToTopButton = document.getElementById('back-to-top');
		
		function updateBackToTop() {
			if (window.pageYOffset > 300) {
				backToTopButton.style.opacity = '1';
				backToTopButton.style.visibility = 'visible';
				backToTopButton.style.transform = 'scale(1)';
			} else {
				backToTopButton.style.opacity = '0';
				backToTopButton.style.visibility = 'hidden';
				backToTopButton.style.transform = 'scale(0.8)';
			}
		}

		// Optimized scroll listener with requestAnimationFrame
		let ticking = false;
		window.addEventListener('scroll', function() {
			if (!ticking) {
				requestAnimationFrame(function() {
					updateBackToTop();
					ticking = false;
				});
				ticking = true;
			}
		});

		// Smooth scroll to top with easing
		backToTopButton.addEventListener('click', function(e) {
			e.preventDefault();
			const duration = 800;
			const start = window.pageYOffset;
			const startTime = performance.now();

			function scrollStep(currentTime) {
				const elapsedTime = currentTime - startTime;
				const progress = Math.min(elapsedTime / duration, 1);
				const easing = 1 - Math.pow(1 - progress, 3); // Cubic ease-out
				
				window.scrollTo(0, start * (1 - easing));
				
				if (progress < 1) {
					requestAnimationFrame(scrollStep);
				}
			}
			
			requestAnimationFrame(scrollStep);
		});

		// Enhanced link hover effects with smooth transitions
		document.querySelectorAll('footer a').forEach(link => {
			link.addEventListener('mouseenter', function() {
				if (this.style.color === 'rgb(209, 213, 219)' || this.style.color === '#D1D5DB' || getComputedStyle(this).color === 'rgb(209, 213, 219)') {
					this.style.color = 'var(--primary-yellow)';
					this.style.textShadow = '0 0 8px rgba(255, 193, 7, 0.5)';
				}
			});
			
			link.addEventListener('mouseleave', function() {
				if (!this.closest('[href^="tel"]') && !this.closest('[href^="#"]')) {
					this.style.color = '#D1D5DB';
					this.style.textShadow = 'none';
				}
			});
		});

